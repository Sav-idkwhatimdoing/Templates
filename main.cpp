void usercontrol(void) {
  while (1) {
    int turn = Controller1.Axis1.position(vex::percent);
    int forward = Controller1.Axis3.position(vex::percent);
    int sideways = Controller1.Axis4.position(vex::percent);

    RightFrontMotor.spin(vex::forward, forward-sideways+turn, vex::percent);
    LeftFrontMotor.spin(vex::forward, forward+sideways-turn, vex::percent);
    RightBackMotor.spin(vex::forward, forward+sideways+turn, vex::percent);
    LeftBackMotor.spin(vex::forward, forward-sideways-turn, vex::percent);


    wait(20, msec); // Sleep the task for a short amount of time to
                    // prevent wasted resources.
  }
}

